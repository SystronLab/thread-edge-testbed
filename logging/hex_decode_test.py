def parse_hex(hex):
    log_bytes = bytes.fromhex(hex)
    
    packet_format = '<HHHBB24s'
    packet_size = struct.calcsize(packet_format)
    
    packets = []
    i = 0
    
    for i in range(0, len(log_bytes), packet_size):
        if i + packet_size > len(log_bytes): 
            break
        
        packet = log_bytes[i:i + packet_size]
        
        packet_array = struct.unpack(packet_format, packet)
        # while packet_array[0] != 23442: # Add feature to check against current device id
        #     i += 1
        #     packet = log_bytes[i:i + packet_size]
        #     packet_array = struct.unpack(packet_format, packet)

        device_id = packet_array[0]
        device_functions = packet_array[1]
        packet_count = packet_array[2]
        device_type = packet_array[3]
        data_len = packet_array[4]
        data_bytes = packet_array[5][:data_len]
        
        log_type_identifier = data_bytes[0:1].decode(errors='ignore') if data_bytes else None
        
        packets.append({
            'deviceId': device_id,
            'deviceFunctions': device_functions,
            'packetCount': packet_count,
            'deviceType': device_type,
            'dataLen': data_len,
            'messageData': data_bytes
        })
    
    return packets
        
